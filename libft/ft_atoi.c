#include "libft.h"

int		ft_atoi(char *str)
{
	int		i;
	int		negative;
	int		res;

	negative = 0;
	res = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
		negative = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	if (negative == 1)
		return (-res);
	return (res);
}
